# Q10. Write a program to perform string operations (slicing, concatenation, reversing).



# string
s = "Alok Kumar"

# Slicing
print("Original String:", s)
print("Slicing (first 4 characters):", s[0:4])
print("Slicing (last 5 characters):", s[-5:])

# Concatenation
s2 = " From Bca"
result = s + s2
print("Concatenation:", result)

# Reversing string
rev = s[::-1]
print("Reversed String:", rev)